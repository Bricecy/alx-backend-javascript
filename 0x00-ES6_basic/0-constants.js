function taskFirst() {
  const firstName = "John";
  const lastName = "Doe";
  console.log(`Hello, ${firstName} ${lastName}`);
}

function taskNext() {
  let count = 0;
  count = count + 1;
  console.log(`Count: ${count}`);
}

taskFirst(); // Output: Hello, John Doe
taskNext();  // Output: Count: 1

